// while

var i = 0; 
console.log("---------Laço While - Início --------- ");
while(i< 10)
{
    console.log(i);
    i++;
}
console.log("---------Laço While - Fim --------- ");


//do... while
var i = 0; 
console.log("---------Laço Do... While - Início --------- ");
do
{
    console.log(i);
    i++;
}while(i < 10);
console.log("---------Laço Do... While - Fim --------- ");