/*Formas de declaração de variáveis */ 

var num = 1; // apesar de não haver necessidade é uma boa prática utilizar 'var' para iniciar uma variável
num = 3; 
var price = 1.5; 
var myName = 'Packt';
var trueValue = true; 
var nullVar = null; 
var und; // sem definição de tipo

console.log('num: '+ num);
console.log('myName: ' + myName);
console.log('trueValue: ' + trueValue);
console.log('price: ' + price);
console.log('nullVar: ' + nullVar);
console.log('und: ' + und);